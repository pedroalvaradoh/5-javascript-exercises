// javascript
// Ejemplo de uso:
// const temperaturaCelsius = convertFToC(68)
// console.log(temperaturaCelsius) // Resultado: 20


const convertFToC = (farenheit) => { 
    celsius = (farenheit - 32) / 1.8
    return celsius;
}


const temperaturaCelsius = convertFToC(68);
